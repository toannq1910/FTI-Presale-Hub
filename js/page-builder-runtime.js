/* v9.9.1 Page Builder Runtime Renderer
   Runtime render for custom pages created in CMS Page Builder.

   Safety rules:
   - Does NOT override existing sidebar routes owned by main router.
   - Only renders custom page routes found in CMS `pages`.
   - By default renders pages with status `active`.
   - Draft pages can be previewed by: #page-preview:<page-id>
*/
import { loadCms, esc } from './cms/cms-core.js';

const ROUTES_OWNED_BY_MAIN_ROUTER = new Set([
  '#overview',
  '#cms',
  '#oncallcx-product-center',
  '#product-detail',
  '#sidebar-alignment',
  '#crm',
  '#integration-playbook',
  '#uc-pbx',
  '#ccaas-vn',
  '#ccaas-global',
  '#api-reference',
  '#video',
  '#demo',
  '#compliance',
  '#resources',
  '#compare'
]);

function normalizeHash(){
  return location.hash || '#overview';
}

function isOwnedRoute(hash){
  if(ROUTES_OWNED_BY_MAIN_ROUTER.has(hash)) return true;
  if(hash.startsWith('#product-detail')) return true;
  return false;
}

function findPage(data, hash){
  const pages = data.pages || [];

  if(hash.startsWith('#page-preview:')){
    const id = hash.replace('#page-preview:', '').trim();
    return pages.find(p => p.id === id) || null;
  }

  if(isOwnedRoute(hash)) return null;

  return pages.find(p => p.route === hash && p.status === 'active') || null;
}

function renderItems(items = [], mode = 'cards'){
  if(!items.length) return '';
  if(mode === 'faq'){
    return items.map(x => `<details class="pb-runtime-faq">
      <summary>${esc(x.q || x.title || 'Câu hỏi')}</summary>
      <p>${esc(x.a || x.content || '')}</p>
    </details>`).join('');
  }
  if(mode === 'table'){
    const keys = Array.from(new Set(items.flatMap(x => Object.keys(x || {}))));
    return `<div class="pb-runtime-table-wrap"><table class="pb-runtime-table">
      <thead><tr>${keys.map(k => `<th>${esc(k)}</th>`).join('')}</tr></thead>
      <tbody>${items.map(row => `<tr>${keys.map(k => `<td>${esc(row?.[k] || '')}</td>`).join('')}</tr>`).join('')}</tbody>
    </table></div>`;
  }
  return `<div class="pb-runtime-card-grid">
    ${items.map(x => `<article class="pb-runtime-card">
      <h3>${esc(x.title || x.q || 'Card')}</h3>
      <p>${esc(x.content || x.a || '')}</p>
      ${x.url ? `<a class="btn btn-soft" href="${esc(x.url)}">Xem thêm</a>` : ''}
    </article>`).join('')}
  </div>`;
}

function renderSection(section){
  const type = section.type || 'text';
  if(type === 'hero'){
    return `<section class="pb-runtime-hero">
      <span class="eyebrow">${esc(section.subtitle || 'Page Builder')}</span>
      <h1>${esc(section.title || '')}</h1>
      <p>${esc(section.content || '')}</p>
      ${section.buttonText ? `<a class="btn btn-primary" href="${esc(section.buttonUrl || '#cms')}">${esc(section.buttonText)}</a>` : ''}
    </section>`;
  }

  if(type === 'cards'){
    return `<section class="pb-runtime-section">
      <h2>${esc(section.title || 'Cards')}</h2>
      ${section.content ? `<p>${esc(section.content)}</p>` : ''}
      ${renderItems(section.items || [], 'cards')}
    </section>`;
  }

  if(type === 'faq'){
    return `<section class="pb-runtime-section">
      <h2>${esc(section.title || 'FAQ')}</h2>
      ${renderItems(section.items || [], 'faq')}
    </section>`;
  }

  if(type === 'cta'){
    return `<section class="pb-runtime-cta">
      <h2>${esc(section.title || '')}</h2>
      <p>${esc(section.content || '')}</p>
      <a class="btn btn-primary" href="${esc(section.buttonUrl || '#cms')}">${esc(section.buttonText || 'Tiếp tục')}</a>
    </section>`;
  }

  if(type === 'table'){
    return `<section class="pb-runtime-section">
      <h2>${esc(section.title || 'Table')}</h2>
      ${renderItems(section.items || [], 'table')}
    </section>`;
  }

  return `<section class="pb-runtime-section">
    <h2>${esc(section.title || '')}</h2>
    <p>${esc(section.content || '')}</p>
  </section>`;
}

function renderPage(page){
  const root = document.querySelector('#pageRoot');
  if(!root || !page) return;

  const title = document.querySelector('#pageTitle');
  const subtitle = document.querySelector('#pageSubtitle');
  if(title) title.textContent = page.title || 'CMS Page';
  if(subtitle) subtitle.textContent = page.description || 'Page Builder Runtime';

  root.innerHTML = `<main class="pb-runtime-page">
    ${(page.sections || []).map(renderSection).join('') || `<div class="cms-empty-state">Page chưa có section.</div>`}
    <section class="pb-runtime-admin">
      <span>Generated by Page Builder</span>
      <a class="btn btn-soft" href="#cms">Chỉnh sửa trong CMS</a>
    </section>
  </main>`;
}

async function tryRenderPageBuilderRoute(){
  const hash = normalizeHash();
  if(!hash || isOwnedRoute(hash)) return;

  try{
    const data = await loadCms();
    const page = findPage(data, hash);
    if(!page) return;
    renderPage(page);
  }catch(err){
    console.warn('[v9.9.1] Cannot render Page Builder route', err);
  }
}

window.addEventListener('hashchange', () => setTimeout(tryRenderPageBuilderRoute, 420));
window.addEventListener('DOMContentLoaded', () => setTimeout(tryRenderPageBuilderRoute, 780));
